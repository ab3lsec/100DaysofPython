# f-Strings in Python
# f-String is a new String formatting technique introduced in Python 3
# To use an f-string we need toi add the letter "f" before declaring a string
# We can use curly braces with the variable name, wherever we want to fetch the value of the variable.

# Syntax: print(f"Hello {name}")

name = "elliot"
hobby = "hacking"
print(f"Hey I'm {name}, I like {hobby}")


# using Expressions in f-String:

print(f"10 x 20 = {10 * 20}")