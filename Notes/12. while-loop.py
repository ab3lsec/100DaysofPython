# While Loops in Python
# While loops are similar to for loops.
# Basic Purpose is to execute a block of code repeatedly when the condition is "True", 
# and once it becomes "False" comes out of the Loop.

# In while Loop, we should initialize an variable with the value forst and then declare the While Loop
# Syntax: while(condition)

i = 0
while(i < 5):
    print(i)
    i += 1




